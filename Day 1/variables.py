# A variable is a memory address where some data is assigned.
name = input ("What is your name?: ")
# here name is the variable or the address where the input of the name is assigned
print(name)
# The variable can be assigned different values and when it is , the data assigned previously is discarded after the line that it is assigned.
# And so here at line 2 The name variable is holding and at line 7 it assigned "name Discarded"
name = "name Discarded"
print(name)


